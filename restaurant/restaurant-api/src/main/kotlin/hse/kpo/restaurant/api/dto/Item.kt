package hse.kpo.restaurant.api.dto

class Item {
    private val id: Long
        get () {
            return this.id
        }

    private val price: Long
        get () {
            return this.price
        }

    private val name: String
        get () {
            return this.name
        }

    private val description: String
        get () {
            return this.description
        }
}